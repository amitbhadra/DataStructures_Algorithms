class Node:
    def __init__(self, data):
        self.data = data
        self.next = None

    def get_data(self):
        return self.data


class LinkedList:
    def __init__(self):
        self.head = None


def print_linked_list(head):
    temp = head
    while temp:
        print(temp.get_data(), end = ' ')
        temp = temp.next
    print ()

